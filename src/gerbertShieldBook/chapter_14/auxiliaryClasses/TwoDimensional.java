package gerbertShieldBook.chapter_14.auxiliaryClasses;

public class TwoDimensional {
    protected double x;
    protected double y;

    public TwoDimensional(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
